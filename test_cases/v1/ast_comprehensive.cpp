class Shape {
public:
  virtual double area() { return 0.0; }
  virtual ~Shape() {}
};

class Circle : public Shape {
public:
  double radius;
  Circle(double r) : radius(r) {}
  double area() override { return 3.14159 * radius * radius; }
};

