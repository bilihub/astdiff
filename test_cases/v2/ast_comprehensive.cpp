int safe_divide(int a, int b) {
  try {
    if (b == 0)
      throw std::runtime_error("divide by zero");
    return a / b;
  } catch (const std::runtime_error &e) {
    std::cout << e.what() << "250" << "260" << std::endl;
    return 0;
  }
}

